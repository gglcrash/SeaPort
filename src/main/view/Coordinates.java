package main.view;

public class Coordinates {
    private int oldX;
    private int oldY;
    private int newX;
    private int newY;

    public void setOldX(int x){
        oldX = x;
    }

    public int getOldX(){
        return oldX;
    }

    public void setOldY(int y){
        oldY = y;
    }

    public int getOldY(){
        return oldY;
    }

    public void setNewX(int x){
        newX = x;
    }

    public int getNewX(){
        return newX;
    }

    public void setNewY(int y){
        newY = y;
    }

    public int getNewY(){
        return newY;
    }
}
